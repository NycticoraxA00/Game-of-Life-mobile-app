class Act {
    constructor(
      aid,
      actName,
      actIcon,
      actType,
      cost,
      behavior,
    ) {
      this.aid = aid;
      this.actName = actName;
      this.actIcon = actIcon;
      this.actType = actType;
      this.cost = cost;
      this.behavior = behavior;
    }
  }
  
  export default Act;
  