class Job {
    constructor(
      jid,
      jobName,
      jobIcon,
      jobType,
      cost,
      salary,
    ) {
      this.jid = jid;
      this.jobName = jobName;
      this.jobIcon = jobIcon;
      this.jobType = jobType;
      this.cost = cost;
      this.salary = salary;
    }
  }
  
  export default Job;
  